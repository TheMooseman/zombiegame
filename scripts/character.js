{
	constructor(level, health, damage, xLocation, yLocation, xAcceleration, yAcceleration, objWidth, objHeight, color, weapon)
	{
		this.level = level;
		this.health = health;
		this.damage = damage;
		this.xLocation = xLocation;
		this.yLocation = yLocation;
		this.xAcceleration = xAcceleration;
		this.yAcceleration = yAcceleration;
		this.objWidth = objWidth;
		this.objHeight = objHeight;
		this.color = color;
		this.weapon = weapon;
	}
}
